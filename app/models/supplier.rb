class Supplier < ApplicationRecord
  ############################################################################################
  # ASSOCIATIONS
  ############################################################################################

  has_many :materials
  has_many :orders, through: :materials

  ############################################################################################
  # VALIDATIONS
  ############################################################################################

  ############################################################################################
  # SCOPES
  ############################################################################################
  scope :by_name, ->(supplier_name) { where('name ILIKE ?', "%#{supplier_name}%") if supplier_name.present? }

  ############################################################################################
  # CALLBACKS
  ############################################################################################

  ############################################################################################
  # INSTANCE METHODS
  ############################################################################################

  ############################################################################################
  # CLASS METHODS
  ############################################################################################
end
