class Client < ApplicationRecord
  ############################################################################################
  # ASSOCIATIONS
  ############################################################################################

  has_many :orders

  ############################################################################################
  # VALIDATIONS
  ############################################################################################

  validates :name, presence: true

  ############################################################################################
  # SCOPES
  ############################################################################################

  scope :by_name, ->(client_name) { where('name ILIKE ?', "%#{client_name}%") if client_name.present? }

  ############################################################################################
  # CALLBACKS
  ############################################################################################

  ############################################################################################
  # INSTANCE METHODS
  ############################################################################################

  ############################################################################################
  # CLASS METHODS
  ############################################################################################
end
